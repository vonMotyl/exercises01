'use strict'

/*Typ liczbowy, number*/

var pensja = 5000;
var premia = 2000;
var calkowitaKwota = pensja + premia;

console.log('Ile piniendzy zarobił ktoś: ' + calkowitaKwota);



/*Typ łańcucha znakow - string*/

var pensjaStr = "5000";
var premiaStr = "2500";

var calkowitaKwotaStr = pensjaStr + premiaStr;

/*Teksty się nie łączą*/
console.log('piniendze w stringach: ' + calkowitaKwotaStr);


/*boolean - yay or nay*/
var czyJestSmog = true;
console.log(czyJestSmog);


/*Typ specjalny - undefined/null*/

var niezdefiniowanaZmienna;
var nullowaZmienna = null;

console.log(niezdefiniowanaZmienna);
console.log(nullowaZmienna);

